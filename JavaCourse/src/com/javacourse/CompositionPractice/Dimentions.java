package com.javacourse.CompositionPractice;

public class Dimentions {

	private int width;
	private int height;
	public Dimentions(int width, int height) {
		super();
		this.width = width;
		this.height = height;
	}
	
	public int getWidth() {
		return width;
	}
	public int getHeight() {
		return height;
	}
}
